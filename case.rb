# case statement

current_weather = 'foggy'

case current_weather
when 'sunny'
	puts "Why are you here on your computer? Get outside!"
when 'rainy'
	puts "Didn't you leave your umbrella at the hotel again?"
when 'foggy'
	puts "Have you ever been to foggy bottom in D.C.? That's what fog makes me thing of."
else
	puts "Hmmm I've never experienced that kind of weather."
end